import Foundation

struct Layout {
    
    // The default diameter of each bubble
    var diameterOfBubble: Float = 50
    
    // The default count of the bubbles ranged horizentally
    var countOfHorizentalBubbles: Int = 0
    
    // The default count of the bubbles ranged vertically
    var countOfVerticalBubbles: Int = 0
    
    // The default margin to the left and right bound
    var marginLeft: Float = 0
    
    // The default margin to the top bound
    var marginTop: Float = 0
    
}
